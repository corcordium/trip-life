function bigSize(){
	oP = document.getElementsByTagName('p');
	for(var i=0;i<oP.length;i++){
		oP[i].style.fontSize = 16 + 'px';
	}
}
function middleSize(){
	oP = document.getElementsByTagName('p');
	for(var i=0;i<oP.length;i++){
		oP[i].style.fontSize = 14 + 'px';
	}
}
function smallSize(){
	oP = document.getElementsByTagName('p');
	for(var i=0;i<oP.length;i++){
		oP[i].style.fontSize = 12 + 'px';
	}
}
